#include<stdio.h>
/*
Estruturas de repetição FOR 
Para i de 1 a 10 Faça	
	bloco do para
Fim para

Escreva um programa que imprima 
os numeros inteiros de 0 a 10.
*/
int main(){
	
	// i++; i = i + 1, incremento da variavel i de 1 em 1
	for(int i=0; i<10; i++){
		printf("%d \n", i);
	}
	
	return 0;
}
